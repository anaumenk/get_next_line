char	*extra_01(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] >= 'A' && s[i] <= 'Z')
			s[i] = s[i] + 'a' - 'A';
		i++;
	}
	i = 0;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\v' || s[i] == '\n')
		i++;
	if (s[i] >= 'a' && s[i] <= 'z')
		s[i] = s[i] + 'A' - 'a';
	return (s);
}
