int	extra_04(char *s)
{
	int i;

	i = 0;
	while (s[i + 1] != '\0')
		i++;
	if (s[i] == ' ' || s[i] == '\t' || s[i] == '\v' || s[i] == '\n')
		return (1);
	return (0);
}
