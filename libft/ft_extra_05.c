#include "libft.h"

int	extra_05(char *s)
{
	int i;
	int j;
	int a;

	i = 0;
	j = 0;
	while ((s[i] == ' ' || s[i] == '\n' || s[i] == '\t') && s[i])
		i++;
	a = i;
	while (s[i + 1] != '\0')
		i++;
	while ((s[i] == ' ' || s[i] == '\n' || s[i] == '\t') && i > 0)
	{
		i--;
		j++;
	}
	return (ft_strlen(s) - a - j);
}
